import * as path from 'node:path';

import { BaseAgentAdapter } from '../shared/base-adapter.js';
import { renderManagedMarkdownDocument } from '../shared/rendering.js';
import type {
  AdapterEnvironment,
  AdapterPlanInput,
  AgentAdapterId,
  SupportLevel,
} from '../shared/types.js';

export class ClaudeCodeAdapter extends BaseAgentAdapter {
  override readonly id: AgentAdapterId = 'claude-code';

  protected override readonly adapterSupportLevel: SupportLevel = 'full';

  protected override readonly detectionConfiguration = {
    executables: ['claude'],
    homeDirectories: ['.claude'],
    homeFiles: ['.claude/CLAUDE.md'],
  };

  protected override async buildPrimaryArtifacts(
    input: AdapterPlanInput,
  ): Promise<
    readonly {
      path: string;
      content: string;
      role: 'rule';
      reason: string;
    }[]
  > {
    return [
      {
        path: path.join(this.workspaceRoot, '.claude', 'rules', 'tavik.md'),
        content: renderManagedMarkdownDocument({
          adapterId: 'claude-code',
          role: 'rule',
          title: 'Org Agent Skills',
          intro:
            'Claude Code rule generated by Org Agent Skills. This stays isolated from user-owned CLAUDE.md files and can be removed safely.',
          baseGuidance: input.baseGuidance,
          skills: input.skills,
        }),
        role: 'rule',
        reason:
          'Render always-on guidance as a project-scoped Claude rule under .claude/rules/.',
      },
    ];
  }
}
